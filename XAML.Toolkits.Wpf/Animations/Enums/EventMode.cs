namespace XAML.Toolkits.Wpf;

/// <summary>
/// event mode
/// </summary>
public enum EventMode
{
    /// <summary>
    /// The none
    /// </summary>
    None,

    /// <summary>
    /// The loaded
    /// </summary>
    Loaded,

    /// <summary>
    /// The unloaded
    /// </summary>
    Unloaded,

    /// <summary>
    /// The mouse enter
    /// </summary>
    MouseEnter,

    /// <summary>
    /// The mouse leave
    /// </summary>
    MouseLeave,

    /// <summary>
    /// The data context changed
    /// </summary>
    DataContextChanged,

    /// <summary>
    /// The got focus
    /// </summary>
    GotFocus,

    /// <summary>
    /// The lost focus
    /// </summary>
    LostFocus,
}
